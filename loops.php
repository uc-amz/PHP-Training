<?php

$a = 5;

// while loop
while ($a <= 10) {
    # code...
}

// do-while loop
do {
    # code...
} while ($a <= 10);

// For loop
for ($i=0; $i < 10; $i++) { 
    echo $i;
}

// foreach loop
// This loop is used with array
$programmingLanguages = ['PHP', 'Java', 'Python'];
foreach ($programmingLanguages as $language) {
    echo $language;
}